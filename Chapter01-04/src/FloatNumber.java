/**
 * 浮点型
 */
public class FloatNumber {

    public static void main(String[] args) {
        double d1=3.14;
        double d2=3.14e2;  //科学计数法
        float f1=2.71f;
        float f3=2.71f;

        System.out.println(d2);
        System.out.println(f1);
        System.out.println("你好"+f3);
    }
}
