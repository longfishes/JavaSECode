/**
 * 测试强制类型转换
 */
public class TypeCast {
    public static void main(String[] args) {
        double e=2.71828;
        int a=(int)e;
        System.out.println(e);
        System.out.println(a);

        int c=97;
        char d=(char)c;
        System.out.println(c);
        System.out.println(d);

        byte b1=(byte)300;
        System.out.println(b1);
    }
}
